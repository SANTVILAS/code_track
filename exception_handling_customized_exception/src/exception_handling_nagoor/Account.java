package exception_handling_nagoor;

public class Account {

	String accNo;
	String accName;
	String accType;
	int balance;
	
	
	public Account(String accNo, String accName, String accType, int balance) {
		super();
		this.accNo = accNo;
		this.accName = accName;
		this.accType = accType;
		this.balance = balance;
	}

	
}
